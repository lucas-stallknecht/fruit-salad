
const Fruit = class {
    constructor(id, name, price, image, color, stock, origin, season){
        this.id = id;
        this.name = name;
        this.price = price;
        this.image = image;
        this.color = color;
        this.stock = stock;
        this.origin = origin;
        this.season = season;
    }
}
export default Fruit